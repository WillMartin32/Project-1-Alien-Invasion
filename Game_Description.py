## Game Description:
## In Alien Invasion, the player controls a rocketship that appears
## at the bottom of the screen. The player can move the ship
## right and left using the arrow keys and shoot bullets using the
## spacebar. When the game begins, a fleet of aliens fills the sky
## and moves across and down the screen. The player shoots and
## destroys the aliens. If the player shoots all the aliens, a new fleet
## appears that move faster than the previous fleet. If any alien hits
## the player's ship or reaches the bottom of the screen, the player
## loses the ship. If the player loses three ships, the game ends.

